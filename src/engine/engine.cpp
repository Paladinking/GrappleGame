extern const int DEFAULT_FONT_LENGTH;
extern const Uint8 DEFAULT_FONT[];



void engine::init() {
	unsigned long buffer_size = 16384;
	Uint8* buffer = new Uint8[buffer_size];
	const int status = uncompress(buffer, &buffer_size, DEFAULT_FONT, DEFAULT_FONT_LENGTH);
	if (status != Z_OK) {
		throw game_exception("Could not load default font");
	}
	SDL_RWops* ptr = SDL_RWFromConstMem(buffer, buffer_size);

	TextBox::init(ptr);
}

